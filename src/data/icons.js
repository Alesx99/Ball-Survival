/** Ritorna l'icona per un upgrade (spell, passive, evolution, mastery, fusion) */
export function getUpgradeIcon(upgradeId, upgrade = null) {
    if (ICONS[upgradeId]) return ICONS[upgradeId];
    if (upgrade?.type === 'fusion') return ICONS[upgradeId] || 'ğŸ”—';
    if (upgrade?.type === 'evolution') return ICONS[upgradeId] || ICONS[upgradeId?.split('_')[0]] || 'â¬†ï¸';
    if (upgrade?.type === 'mastery') return ICONS[upgradeId] || ICONS[upgradeId?.split('_')[0]] || 'ğŸ‘‘';
    if (upgrade?.type === 'passive') return ICONS[upgradeId] || 'ğŸ“Œ';
    const baseId = upgrade?.primary || upgradeId?.split('_')[0];
    return ICONS[baseId] || ICONS[upgradeId] || 'âœ¨';
}

